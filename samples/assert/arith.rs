
fn main() {
    // add integers
    assert!(1+0 == 1);
    assert!(1+2 == 3);
    assert!(3+4 == 7);
    
    // substract integers
    assert!(1-0 == 1);
    assert!(3-2 == 1);
    
    // multiple integers
    assert!(2*3 == 6);
    assert!(3*4 == 12);
    
    // divide integers
    assert!(6/2 == 3);
    assert!(9/3 == 3);
    
    // mod integers
    assert!(3%2 == 1);
    assert!(8%4 == 0);
}